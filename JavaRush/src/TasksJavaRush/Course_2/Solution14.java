/*
Реализуй метод print3.
Метод должен вывести на экран переданную строку 3 раза.
Каждый раз с новой строки.
 */
public class Solution14 {
    public static void print3(String s) {
        System.out.println(s);
        System.out.println(s);
        System.out.println(s);

    }
    public void javaRush(){
        print3("I love you!");
    }

}
